export const hasPermission = (permission: Permission | Permission[], objectPermissions: Permission[] = []) => {
    if (Array.isArray(permission)) {
        if (permission.some((p) => hasPermission(p, objectPermissions))) {
            return true;
        }

        return false;
    }

    return objectPermissions.includes(permission);
};

